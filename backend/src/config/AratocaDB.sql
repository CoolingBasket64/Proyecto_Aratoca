drop database aratoca;
create database aratoca;
use aratoca;

CREATE TABLE ttipo_doc(
    cod_tipo_doc INT PRIMARY KEY auto_increment,
    descripcion_min VARCHAR(10) NOT NULL,
    descripcion VARCHAR(50)
);

CREATE TABLE ubicaciones (
    id_ubicacion INT PRIMARY KEY,
    zona VARCHAR(20),
    vereda VARCHAR(100),
    sector VARCHAR(100),
    finca VARCHAR(100),
    barrio VARCHAR(100),
    direccion VARCHAR(200)
);

ALTER TABLE ubicaciones
ADD cod_sector VARCHAR(20);


CREATE TABLE personas_discapacidad (
    id_persona INT PRIMARY KEY,
    codigo VARCHAR(20),
    cod_tipo_doc INT,
    documento VARCHAR(20),
    primer_apellido VARCHAR(100),
    segundo_apellido VARCHAR(100),
    primer_nombre VARCHAR(100),
    segundo_nombre VARCHAR(100),
    nombre_completo VARCHAR(200),
    fecha_nacimiento DATE,
    edad INT,
    sexo CHAR(1),
    discapacidad VARCHAR(150),
    rlcpd VARCHAR(10),
    celular VARCHAR(20),
    tiene_cuidador BOOLEAN,
    id_ubicacion INT,
    FOREIGN KEY (cod_tipo_doc) REFERENCES ttipo_doc(cod_tipo_doc),
    FOREIGN KEY (id_ubicacion) REFERENCES ubicaciones(id_ubicacion)
);

ALTER TABLE personas_discapacidad
ADD activo BOOLEAN DEFAULT 1,
ADD fecha_modificacion TIMESTAMP 
DEFAULT CURRENT_TIMESTAMP 
ON UPDATE CURRENT_TIMESTAMP,
ADD razon_estado VARCHAR(255);

CREATE TABLE cuidadores (
    id_cuidador INT PRIMARY KEY,
    id_persona INT,
    cod_tipo_doc INT,
    documento VARCHAR(20),
    primer_apellido VARCHAR(100),
    segundo_apellido VARCHAR(100),
    primer_nombre VARCHAR(100),
    segundo_nombre VARCHAR(100),
    nombre_completo VARCHAR(200),
    fecha_nacimiento DATE,
    edad INT,
    sexo CHAR(1),
    parentesco VARCHAR(100),
    celular VARCHAR(20),
    FOREIGN KEY (id_persona) REFERENCES personas_discapacidad(id_persona),
    FOREIGN KEY (cod_tipo_doc) REFERENCES ttipo_doc(cod_tipo_doc)
);


CREATE TABLE usuarios (
    id_usuario INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    email VARCHAR(150) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    rol VARCHAR(20) DEFAULT 'admin',
    estado BOOLEAN DEFAULT TRUE,
    fecha_modificacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

